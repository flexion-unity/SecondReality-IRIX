    void init_uframe(const void * fseg)
    {
        aseg = (const uint8_t *)fseg;
    }

    void ulosta_frame()
    {
        uint8_t * dest = Shim::vram;

        const uint8_t * src = aseg;

        for (bool finished = false; !finished;)
        {
            int val = (int8_t)*src++;

            if (val == 0)
            {
                break;
            }

            if (val < 0)
            {
                do
                {
                    dest += (size_t)(-val);
                    val = (int8_t)*src++;
                    if (val == 0)
                    {
                        finished = true;
                        break;
                    }
                } while (val < 0);
            }

            if (!finished)
            {
                uint8_t repeatValue = *src++;
                memset(dest, repeatValue, (size_t)val);
                dest += (size_t)val;
            }
        }

        size_t delta = (size_t)(src - aseg);

        if (size_t mod16 = (delta & 15u); mod16)
        {
            delta += (16u - mod16);
        }

        aseg += delta;
    }

    void main()
    {
        if (debugPrint) printf("Scene: PAM/OUTTAA\n");
        CLEAR(pam_pal);

        memcpy(pam_pal, Data::basePalette, sizeof(Data::basePalette));

        int f{}, p{};
        unsigned b{};

        if (!Shim::isDemoFirstPart())
        {
            while (Music::sync() < 10 && !demo_wantstoquit())
            {
                AudioPlayer::Update(true);
            }
        }

        Shim::clearScreen();

        for (int a = 1; a < 64; a++)
        {
            for (b = 0; b < PaletteByteCount; b++)
            {
                pam_pal[a * PaletteByteCount + b] = static_cast<char>((63 * a + (64 - a) * pam_pal[b]) / 64);
            }
        }

        init_uframe((char *)Data::pam_memblock);

        while (!demo_wantstoquit() && f++ < 45)
        {
            if (f <= 40)
            {
                if (p)
                {
                    p = 0;
                    ulosta_frame();
                }
                else
                {
                    p = 1;
                    ulosta_frame();
                }
            }

            Common::cop_pal = &pam_pal[PaletteByteCount * Data::wfade[f]];

            Common::do_pal = 1;

            Common::copper2();
            Common::copper3();

            for (int i = 0; i < 4; i++)
            {
                demo_vsync(true);
            }

            demo_blit();
        }
    }
